test_that("potential energy of triads in harmony context works as expected",{
  expect_equal(potential_energy(from=c(0,3,7),to=c(0,3,7),home=0),0)
  expect_equal(potential_energy(from=c(0,4,7),to=c(0,4,7),home=0),0)
  expect_equal(potential_energy(from=c(12,8,5),to=c(12,8,5),home=0),0)
  expect_equal(potential_energy(from=c(0,3,7),to=c(0,3,7),home=12),0)
  expect_equal(potential_energy(from=c(0,4,7),to=c(0,4,7),home=12),0)
  expect_equal(potential_energy(from=c(12,8,5),to=c(12,8,5),home=12),0)
  expect_equal(potential_energy(from=c(7,11,14),to=c(0,4,7),home=0),61.53,tolerance=.001)
  expect_equal(potential_energy(from=c(5,1,-2),to=c(12,8,5),home=12),61.53,tolerance=.001)
})
